    private void aktualizujPrzypomnienia() {
        ArrayList<Przypomnienie> nowePrzypomnienia = new ArrayList<>();

        // wydatki stałe
        Calendar cal = Calendar.getInstance();
        int currentDayOfMonth = cal.get(Calendar.DAY_OF_MONTH);

        Wydatki.pobierzStaleObj().forEach(w -> {
            Calendar cal2 = Calendar.getInstance();
            cal2.setTime(w.getData());
            int dayOfMonthWydatekStaly = cal2.get(Calendar.DAY_OF_MONTH);

            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            String formatted = format.format(cal.getTime());

            if (currentDayOfMonth == dayOfMonthWydatekStaly) {
                nowePrzypomnienia.add(new Przypomnienie("Dzisiaj jest do wykonania wydatek stały: " +
                        w.getNaCo() + " (" + w.getWartosc() + " zł, " +
                        formatted + ")", "Wydatek stały: " + w.getNaCo(), w.getId()));
            }
        });

        for (Przypomnienie przypomnienie : nowePrzypomnienia) {

            if (Przypomnienia.pobierzWszystkie().stream().noneMatch(p -> p.getTytul().equals(przypomnienie.getTytul()))) {
                Przypomnienia.dodaj(przypomnienie);
            }
        }

        List<String> przypomnienia = Przypomnienia.pobierzWszystkie().stream()
                .map(Object::toString).collect(Collectors.toList());
        lstPowiadomienia.setListData(przypomnienia.toArray(new String[0]));
    }

