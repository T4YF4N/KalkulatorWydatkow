    public void aktualizujKwoty() {
        String suma;
        List<String> kwoty;
        switch (kwotaTryb) {
            case kwotaGotowka:
                suma = Kwota.sumujGotowke().toString() + " zł";
                txtDostepnaKwota.setText(suma);
                kwoty = Kwota.pobierzKwotyGotowka();
                lstKwoty.setListData(kwoty.toArray(new String[0]));
                break;
            case kwotaKonta:
                suma = Kwota.sumujPieniadzeNaKontach().toString() + " zł";
                txtDostepnaKwota.setText(suma);
                kwoty = Kwota.pobierzKwotyKonta();
                lstKwoty.setListData(kwoty.toArray(new String[0]));
                break;
            case kwotaWszystko:
                suma = Kwota.sumujWszystkieKwoty().toString() + " zł";
                txtDostepnaKwota.setText(suma);
                kwoty = Kwota.pobierzKwotyWszystkie();
                lstKwoty.setListData(kwoty.toArray(new String[0]));
                break;
        }
    }

    public void aktualizujWydatki() {
        String suma;
        List<String> wydatki;
        switch (wydatkiTryb) {
            case wydatkiZwykle:
                suma = Wydatki.sumujZwykle().toString() + " zł";
                txtSumaWydatkow.setText(suma);
                wydatki = Wydatki.pobierzWydatkiZwykle();
                lstWydatki.setListData(wydatki.toArray(new String[0]));
                break;
            case wydatkiStale:
                suma = Wydatki.sumujStale().toString() + " zł";
                txtSumaWydatkow.setText(suma);
                wydatki = Wydatki.pobierzStale();
                lstWydatki.setListData(wydatki.toArray(new String[0]));
                break;
            case wydatkiWszystkie:
                suma = Wydatki.sumujWszystkie().toString() + " zł";
                txtSumaWydatkow.setText(suma);
                wydatki = Wydatki.pobierzWszystkie();
                lstWydatki.setListData(wydatki.toArray(new String[0]));
                break;
        }

        wydatki = Wydatki.pobierzWszystkie();
        lstDokumentyWydatki.setListData(wydatki.toArray(new String[0]));
    }

