    private void btnZapiszDokumentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZapiszDokumentActionPerformed
        String selectedValue = lstDokumenty.getSelectedValue();
        if (selectedValue == null) {
            Sys.warning("Zaznaczenie", "Nie zaznaczono pozycji");
            return;
        }

        int selectedId = Integer.parseInt(selectedValue.split("]")[0].substring(1));

        Dokument dokument = DokumentService.pobierz(selectedId);

        if (dokument == null) {
            return;
        }

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Wybierz miejsce zapisania pliku");
        fileChooser.setSelectedFile(new File(dokument.getNazwaPliku()));

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();

            OutputStream os;
            try {
                os = new FileOutputStream(fileToSave);
                os.write(dokument.getDane());
                os.close();

                Sys.info("Zapisywanie pliku", "Zapisano plik jako: " + fileToSave.getAbsolutePath());
            } catch (IOException e) {
                Sys.warning("Zapisywanie pliku", "Wystąpił problem z zapisem pliku");
            }
        }

    }//GEN-LAST:event_btnZapiszDokumentActionPerformed

